    private void cboDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboDatosActionPerformed
        String mensaje = cboDatos.getSelectedItem().toString();
        if (mensaje.equals("ID")) {
            etiResultado.setText(" " + id );
        }else {
            if (mensaje.equals("NOMBRE")){
                etiResultado.setText(" " + nombre );
            }else{ 
                if (mensaje.equals("APELLIDO")) {
                    etiResultado.setText(" " + apellido );
                } else{                   
                    if (mensaje.equals("NACIMIENTO")){
                        etiResultado.setText(" " + fnaci );
                    }else{
                        if (mensaje.equals("SEMESTRE")){
                            etiResultado.setText(" " + semestre );
                        }else{
                            if (mensaje.equals("EMAIL")) {
                                etiResultado.setText(" " + email );
                            }else{
                                if (mensaje.equals("GENERO")) {
                                    etiResultado.setText(" " + genero );
                                }else{
                                    if (mensaje.equals("TELEFONO")){
                                        etiResultado.setText(" " + telefono );
                                    }else{
                                        if (mensaje.equals("SEMESTRE")){
                                            etiResultado.setText(" " + semestre );
                                        }else{
                                            if (mensaje.equals("PROGRAMA")) {
                                                etiResultado.setText(" " + programa );
                                            }else{
                                                if (mensaje.equals("UNIVERSIDAD")){
                                                    etiResultado.setText(" " + universidad );
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }                   
                }     
            }
        }
    }//GEN-LAST:event_cboDatosActionPerformed

