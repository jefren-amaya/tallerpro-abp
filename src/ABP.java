        nombre = txtNombre.getText();
        for(int con = 0; con < nombre.length(); con ++){
            char carac = nombre.charAt(con);
            if (Character.isDigit(carac)){
                nodi = 1;
            }
        }
        apellido = txtApellidos.getText();
